"""Последствия смягчения риск-правил на НАШИХ данных (вариант 3).

Калибровка строго по эмпирике сессии: лучшее, что нашли —
≈нулевой край (trend_ema портфель PF≈1.12, mean/сделку ≈ 0;
funding доказано net-отриц.). Берём ДВА честных сценария:
  A) zero-edge: mean=0 (оптимистично — наш потолок);
  B) реалистичный: mean=−издержки (то, что фактически получалось).
Волатильность сделки ≈ из наших крипто-прогонов (σ≈6%/сделку).

Monte-Carlo 10k путей × 200 сделок (~1 год). Сравниваем риск-
профили: текущий (1%/сделку, плечо 1x, мес-стоп −15%) vs
смягчённый (5/10%/сделку, плечо 5/25x, без стопов).
Метрика: P(разорение = эквити <10% старта) и медиана итога.

Вывод не риторика — арифметика на наших же числах.
"""

from __future__ import annotations

import random

_N_PATHS = 10_000
_N_TRADES = 200  # ~1 год
_SIG = 0.06  # σ доходности сделки (из крипто-прогонов сессии)
_COST = 0.0015  # реалистичные round-trip издержки
_RUIN = 0.10  # «разорение» = эквити < 10% старта
random.seed(42)


def _sim(mean: float, risk_frac: float, lev: float, monthly_stop: bool) -> tuple[float, float]:
    """Возвращает (P(разорение), медиана итог-эквити) по Monte-Carlo."""
    ends: list[float] = []
    ruin = 0
    for _ in range(_N_PATHS):
        eq = 1.0
        month_start = 1.0
        for k in range(_N_TRADES):
            # доходность сделки ~ N(mean, σ); ставим risk_frac·lev эквити
            r = random.gauss(mean, _SIG)
            eq *= 1.0 + risk_frac * lev * r
            if eq <= _RUIN:
                eq = 0.0
                break
            if monthly_stop and k % 20 == 19:  # ~месяц
                if eq <= month_start * 0.85:  # −15% мес-стоп → пауза (стоп пути)
                    break
                month_start = eq
        ends.append(eq)
        if eq <= _RUIN:
            ruin += 1
    ends.sort()
    return ruin / _N_PATHS, ends[_N_PATHS // 2]


def main() -> None:
    print("Последствия риск-правил на НАШИХ данных (Monte-Carlo 10k × 200 сделок)")
    print("σ/сделку=6% (из крипто-прогонов). Разорение = эквити <10% старта.")
    for label, mean in [
        ("A) zero-edge (наш ПОТОЛОК, mean=0)", 0.0),
        ("B) реалистичный (mean=−издержки)", -_COST),
    ]:
        print(f"\n=== {label} ===")
        print(f"{'профиль':38s} | P(разорение) | медиана итог")
        print("-" * 70)
        configs = [
            ("ТЕКУЩИЙ: 1%/сделку, 1x, мес-стоп", 0.01, 1.0, True),
            ("риск 5%/сделку, 1x, без стопов", 0.05, 1.0, False),
            ("риск 10%/сделку, 1x, без стопов", 0.10, 1.0, False),
            ("риск 5%/сделку, 5x, без стопов", 0.05, 5.0, False),
            ("риск 10%/сделку, 25x, без стопов", 0.10, 25.0, False),
        ]
        for name, rf, lv, ms in configs:
            pr, med = _sim(mean, rf, lv, ms)
            print(f"{name:38s} |   {pr * 100:5.1f}%    |  ×{med:6.3f}")
    print(
        "\nЧитается так: при крае ≤0 (а это наш доказанный потолок) рост\n"
        "риска/плеча и снятие стопов МОНОТОННО повышают P(разорения) и\n"
        "топят медиану. Прибыль не появляется (её неоткуда взять) —\n"
        "ускоряется и углубляется потеря. Это арифметика, не мнение."
    )


if __name__ == "__main__":
    main()
